int	ft_is_prime(int nb)
{
	int	i;
	int	c;

	i = 1;
	c = 0;
	if (nb == 1 || nb == 0)
		return (0);
	else if (nb == 2)
		return (1);
	while (i <= nb)
	{
		if (nb % i == 0)
			c++;
		i++;
	}
	if (c == 2)
		return (1);
	else
		return (0);
}
